map_cells = []

def get_map_size(mapi):
    return len(mapi[1]) * sell_size[0], len(mapi) * sell_size[1]

def write_map(mapi):
    for y in range(len(mapi)):
        for x in range(len(mapi[y])):
            if mapi[y][x] == 1:
                n = random.randint(0, 4)
                collide_tiles.add(Tile(x * sell_size[0] * scale_x, y * sell_size[1] * scale_y,
                                       (sell_size[0] * scale_x, sell_size[1] * scale_y), stone_wall_images[n].convert_alpha()))
            if mapi[y][x] == 9: #хилки спавн
                item_group.add(Item((x * sell_size[0] * scale_x, y * sell_size[1] * scale_y), item_types["heal"], 0.4))
            map_cells.append(Cell(x * sell_size[0] * scale_x, y * sell_size[1] * scale_y))

class Cell:
    def __init__(self, x, y):
        self.x = x
        self.y = y

def get_map_cell(x, y):
    for cell in map_cells:
        if cell.x == x and cell.y == y:
            return cell

loaded_map = m0_0
def load_map(mapi):
    for i in collide_tiles:
        i.remove(collide_tiles)
    map_cells.clear()
    global loaded_map
    loaded_map = mapi
    write_map(loaded_map)


